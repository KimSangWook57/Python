import pandas as pd

df = pd.read_csv('일별평균대기오염도_2022.csv', encoding="cp949")
# df의 정보
print(df.info())
print("========================================================================")
print("========================================================================")
# iloc를 이용한 열 출력
name_age_col = df.iloc[:, [0, 1]]
print(name_age_col)
print("========================================================================")
print("========================================================================")
# 대괄호를 이용한 여러 조건을 충족하는 열 선택
sub_df = df.loc[(df['미세먼지농도(㎍/㎥)'] >= 30) & (df['오존농도(ppm)'] >= 0.001)]
print(sub_df)
print("========================================================================")
print("========================================================================")
# 대괄호를 이용한 조건에 맞는 복수 열 선택
# sub_df = df.loc[(df['미세먼지농도(㎍/㎥)'] >= 30)][['측정일시', '측정소명', '미세먼지농도(㎍/㎥)']]
# 위 코드보다 조금 더 명시적인 코드
sub_df = df.loc[(df['미세먼지농도(㎍/㎥)'] >= 30)].loc[:,['측정일시', '측정소명', '미세먼지농도(㎍/㎥)']]
print(sub_df)
print("========================================================================")
print("========================================================================")
# 부분 데이터 선택 방법 3: 슬라이싱을 이용한 범위 지정
sub_df_3 = df.iloc[1:13, 1:13]
print(sub_df_3)
print("========================================================================")
print("========================================================================")
# 부분 데이터 선택 방법 4: iloc[]를 이용한 인덱스 지정
sub_df_4 = df.iloc[[1, 4], [0, 3]]
print(sub_df_4)
print("========================================================================")
print("========================================================================")